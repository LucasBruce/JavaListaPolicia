package br.com.bruce.bancoPoliciais;

public class Policial {

	private String nome;

	private String corFarda;

	private String arma;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCorFarda() {
		return corFarda;
	}

	public void setCorFarda(String corFarda) {
		this.corFarda = corFarda;
	}

	public String getArma() {
		return arma;
	}

	public void setArma(String arma) {
		this.arma = arma;
	}

}
